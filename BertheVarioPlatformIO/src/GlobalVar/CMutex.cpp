#include "../BertheVario.h"

////////////////////////////////////////////////////////////////////////////////
/// \brief Init du mutex
CMutex::CMutex()
{
m_shared_var_mutex = xSemaphoreCreateMutex() ;
}

////////////////////////////////////////////////////////////////////////////////
/// \brief
void CMutex::PrendreMutex()
{
xSemaphoreTake(m_shared_var_mutex, portMAX_DELAY) ;
}

////////////////////////////////////////////////////////////////////////////////
/// \brief
void CMutex::RelacherMutex()
{
xSemaphoreGive(m_shared_var_mutex);
}
